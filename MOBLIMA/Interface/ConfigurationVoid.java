package MOBLIMA.Interface;

/**
 * interface implemented for add/update/remove methods for concrete classes
 */
public interface ConfigurationVoid {
    /**
     * add method that returns void
     */
    public abstract void add();

    /**
     * update method that returns void
     */
    public abstract void update();

    /**
     * remove method that returns void
     */
    public abstract void remove();
}
